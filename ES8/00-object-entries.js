// Object entries - Para obtener los elementos de un objecto, dividido en diferentes arrays
const family = {
    mom: 'Mayra',
    dad: 'David',
    sister: 'Galilea',
    brother: 'Mateo',
    grandfather: 'Walter',
    grandmother: 'Idalia'
}
console.log(Object.entries(family));

// Object values - Para obtener los valores de cada elemento en un solo array
const zoo = {
    tiger: '🐯',
    cat: '🐱',
    owl: '🦉',
    unicorn: '🦄',
    cow: '🐄',
    chicken: '🐤'
}
console.log(Object.values(zoo));